# [문제 4] 작업 스케줄 안정 구간
#
# 길이 N의 작업 배열 A가 있다. A[i]는 i번째 작업의 난이도이다.
#
# 연속 구간 [l, r]를 하나 선택했을 때,
# 그 구간의 안정도는 다음과 같이 정의한다.
#
# 안정도([l, r]) = (구간 내 최댓값 - 구간 내 최솟값) * (r - l + 1)
#
# 당신은 길이가 L 이상 R 이하인 모든 구간 중에서,
# 안정도가 X 이하인 구간의 개수를 구해야 한다.
#
# 입력 형식
# - 첫 줄: N L R X
# - 둘째 줄: A1 A2 ... AN
#
# 출력 형식
# - 조건을 만족하는 구간의 개수
#
# 제한
# - 1 <= N <= 300000
# - 1 <= L <= R <= N
# - 1 <= Ai <= 10^9
# - 0 <= X <= 10^18
#
# 예시
# 입력
# 7 2 4 8
# 3 1 4 1 5 9 2
#
# 출력
# 6
#
# 설명
# - 구간 길이 제약과 (max-min) 값이 동시에 걸려 있어
#   투 포인터/자료구조 조합 최적화가 필요한 문제다.