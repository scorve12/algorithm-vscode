# [문제 2] 로그 압축 구간 점수
#
# 길이 N의 문자열 S(영문 소문자)와 정수 P가 주어진다.
# 문자열을 연속한 여러 구간으로 분할한 뒤, 각 구간을 아래 규칙으로 압축한다.
#
# - 한 구간에서 같은 문자가 연속으로 c번 나오면, 압축 길이 기여도는
#   c == 1 이면 1, c >= 2 이면 1 + 자릿수(c)
#
# 구간 점수는 "원래 구간 길이 - 압축 길이"이며,
# 전체 점수는 모든 구간 점수의 합이다.
#
# 단, 분할한 구간의 개수는 정확히 P개여야 한다.
#
# 가능한 전체 점수의 최댓값을 구하라.
#
# 입력 형식
# - 첫 줄: N P
# - 둘째 줄: S
#
# 출력 형식
# - 최대 전체 점수
#
# 제한
# - 1 <= N <= 4000
# - 1 <= P <= min(200, N)
# - S는 소문자 알파벳
#
# 예시
# 입력
# 8 2
# aaabccdd
#
# 출력
# 3
#
# 설명
# - 분할을 적절히 잡으면 각 구간의 반복 압축 이득을 크게 만들 수 있다.