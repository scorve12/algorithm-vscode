# [문제 3] 교차 금지 배송 경로
#
# 2차원 격자 위에 시작점 S와 도착점 T, 그리고 장애물(#)이 주어진다.
# 당신은 S에서 T까지 정확히 두 번 이동해야 한다.
#
# 1차 이동 경로와 2차 이동 경로는 다음 조건을 만족해야 한다.
# - 각 경로는 상하좌우 인접 칸으로만 이동
# - 장애물 칸은 지나갈 수 없음
# - 두 경로가 공유할 수 있는 칸은 S와 T만 허용
#
# 각 이동의 비용은 "이동한 칸 수"이며,
# 두 이동 비용 합의 최솟값을 구하라.
# 불가능하면 -1을 출력하라.
#
# 입력 형식
# - 첫 줄: H W
# - 다음 H줄: 격자 정보 ('.', '#', 'S', 'T')
#
# 출력 형식
# - 가능한 경우 최소 비용, 불가능하면 -1
#
# 제한
# - 2 <= H, W <= 200
# - S, T는 정확히 하나씩 존재
#
# 예시
# 입력
# 5 6
# S..#..
# .##...
# ..#..T
# ..#...
# ......
#
# 출력
# 18
#
# 설명
# - 두 번의 왕복이 아니라, S->T를 두 번 수행한다고 가정한다.
# - 경로 간 교차 제한 때문에 단순 최단거리*2와 달라질 수 있다.